const { part1, part2 } = require("./solution")

describe("part1", () => {
  test("returns sum of part numbers", () => {
    const input = `467..114..
...*......
..35..633.
......#...
617*......
.....+.58.
..592.....
......755.
...$.*....
.664.598..`
    expect(part1(input)).toEqual(4361)
  })
})

